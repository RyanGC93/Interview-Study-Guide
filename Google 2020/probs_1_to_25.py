# 1.FIND MEDIAN SORTED ARRAYS ==========================================================
class Solution:
  def findMedianSortedArrays(self, nums1: List[int], nums2: List[int]) -> float:
      nums = nums1+nums2
      nums.sort()
      ln=len(nums)
  
      if ln%2==1:
          return nums[(ln//2)]
      else:
          return (nums[(ln//2)]+nums[(ln//2)-1])/2
